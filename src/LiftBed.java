public class LiftBed extends Bed {

    private int angle;
    private int cargoHeight;

    public void setCargoHeight(int cargoHeight) {
        this.cargoHeight = cargoHeight;
    }

    public void setAngle(int angle) {
        this.angle = angle;
    }

    public int getCargoHeight() {
        return cargoHeight;
    }

    public int getAngle() {
        return angle;
    }

}
